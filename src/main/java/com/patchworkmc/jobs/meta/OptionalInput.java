package com.patchworkmc.jobs.meta;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.FIELD, ElementType.METHOD})
public @interface OptionalInput {
	boolean DEFAULT_BOOLEAN = false;
	byte DEFAULT_BYTE = 0;
	char DEFAULT_CHAR = 0;
	short DEFAULT_SHORT = 0;
	int DEFAULT_INT = 0;
	long DEFAULT_LONG = 0;
	float DEFAULT_FLOAT = 0;
	double DEFAULT_DOUBLE = 0;
	Void DEFAULT_VOID = null;
	String DEFAULT_STRING = null;
}
